# Toán tử logic
x = 10
y = 5
z = 10
print(x == z and y == z)  # chỉ trả về True khi cả 2 mệnh đề là True
print(x == z and y < z)

print(x == z or y == z)  # sẽ trả về True khi chỉ cần 1 trong 2 mệnh là True
print(not x == z)  # phủ định của kết quả

