# Toán tử số học
a = 10
b = 7
print('toán tử cộng', a + b)  # toán tử cộng
print('toán tử trừ', a - b)  # toán tử trừ
print('toán tử nhân', a * b)  # toán tử nhân
print('toán tử chia', a / b)  # toán tử chia
print('toán tử chia lấy dư', a % b)  # toán tử chia lấy dư
print('toán tử lũy thừa', a ** b)  # toán tử lũy thừa
print('toán tử chia làm tròn xuống', a // b)  # toán tử chia làm tròn xuống

# Toán tử quan hệ
print('Kết so sánh a = b?', a == b)
print('Kết so sánh a # b?', a != b)
print('Kết so sánh a > b?', a > b)
print('Kết so sánh a < b?', a < b)
print('Kết so sánh a >= b?', a >= b)
print('Kết so sánh a <= b?', a <= b)

# Toán tử gán
c = 7  # gán giá trị 7 cho biến c
c += 3  # c = c+3
print(c)
c -= 3  # c = c-3
print(c)
